                    <h5>But, How can you prepare Dona Pepa Turron?</h5>
                    <br>
                    <p>Nowadays, turron is widely available to buy in bakeries, shops, and supermarkets, but there is always an option to make it at home. The following is a traditional Turron recipe: </p>
                    <br>
                    <p><strong>Ingredientes:</strong></p>
                    <p><strong>El Turrón:</strong></p>
                    <ul>
                        <li>5 tazas de harina sin preparar</li>
                        <li>2 tazas de manteca (shortening/butter/lard)</li>
                        <li>1 taza de mantequilla </li>
                        <li>3 yemas de huevo (egg yolk)</li>
                        <li>4 cucharaditas de azúcar </li>
                        <li>1 cucharaditas de ajonjolí tostado y molido (grounded, roasted sesame)</li>
                        <li>2 cucharaditas de polvo de hornear (baking powder)</li>
                        <li>1 cucharadita de anís molido (ground Anise)</li>
                        <li>5 cucharadita de agua de anís (anis wáter) This can be made at home or bought</li>
                        <li>1 cucharadita de sal </li>
                    </ul>
                    <br>
                    <p><strong>La miel:</strong></p>
                    <ul>
                        <li>1 bola de chancaca (550g) (Panela, dark Brown sugar, molasses, substitute)</li>
                        <li>½ taza de agua </li>
                        <li>¼ de rodaja de naranja (orange slices)</li>
                        <li>1 limón</li>
                        <li>2 membrillos (Quince cheese is a sweet, thick jelly made of the pulp of the quince fruit)</li>
                        <li>1 plátano (plantain)</li>
                        <li>½ kilo de melocotones pequeños (peaches)</li>
                        <li>100 gr de higos secos (dried figs)</li>
                        <li>2 manzanas (apples)</li>
                        <li>6 clavos de olor (cloves)</li>
                        <li>3 palitos canelas (Cinnamon sticks) y 3 anís estrella (star anis)</li>
                    </ul>
                    <br>
                    <p><strong>Preparación:</strong></p>
                    <p>Para la masa:</p>
                    <ol>
                        <li>En un bol cernir la harina, la sal y añadir la mantequilla a temperatura ambiente, incorporar gradualmente las yemas de huevo y el agua de anís.</li>
                        <li>Amasar hasta obtener una mezcla suave y homogénea. </li>
                        <li>Formar rollos de 1 cm de diámetro por 18 cm de largo, colocarlos en una fuente para horno engrasada con una ligera separación entre cada uno.</li>
                        <li>Finalmente, hornear a 175° C durante 25 minutos, hasta que esté ligeramente dorado y luego de ello, dejar enfriar.</li>
                    </ol>
                    <br>
                    <p><strong>Para la miel: </strong></p>
                    <ol>
                        <li>Hervir por aproximadamente 30 minutos, 1 bola de chancaca (550 gramos), ½ taza de agua, y todos los ingredientes hasta que la fruta se haya desecho y el líquido se reduzca a la mitad.</li>
                        <li>Mover la preparación que debe encontrarse a 113° C en termómetro. </li>
                    </ol>
                    <p>Si no se tiene a la mano un termómetro de caramelo, llenar un vaso de agua fría y echar una gota de la miel observando si se forma una masa suave.</p>
                    <br>
                    <p><strong>Para el turrón:</strong></p>
                    <ol>
                        <li>En una fuente colocar una capa de rollos muy juntos, uno al lado del otro y rociar con la miel fría. Repetir la operación colocando rollos en forma contraria a la capa anterior consecutivamente hasta que tenga tres niveles.</li>
                        <li>Añadir grageas de colores y frutos secos a gusto.</li>
                    </ol>
                    <br>
                    <h5>Finally, Enjoy!</h5>
                    <br>
                    <p>Turron is not only a dessert, rather it is part of our national identity, a source of pride for Peruvians. </p>
                    <p>For this reason, every tourist and foreigner who finds themselves in Peru, who is experiencing the culinary delights Peru has to offer, must try this dessert that is so tied to Peruvian culture and Religion. You will not regret it! </p>
                    <p>To eat a Dona Pepa Turron, can transport a Peruvian person to a time when she/he sat down with their family one cold evening, surrounded by sweet smells in the air as the dessert is being prepared, with a cup of coffee or hot chocolate in hand, and more than one story to tell. It can bring your back to your youth and how you couldn’t wait for your grandparents to arrive with sweets, churros/crepes, and a box of Turrones which disappeared immediately once in the hands of the grandchildren. Turron is reminiscent of the happy moments in life. It can make you feel nostalgic and long for more time with family. Doña Pepa turron is togetherness, it’s Peru. </p>
