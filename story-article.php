                    <p> Over the years, other names have been given to these sweets, such as <strong>“Turron del Senor de los Milagros”</strong> or <strong>“Turron de Miel”</strong>, but unlike those names, the name <strong>“Turron de Dona Pepa”</strong> has lasted the test of time and is known as such not only in Peru but in the world. </p>
                    <p>This sweet is so representative of Peru, that in October of 2009, someone prepared the world’s biggest Turron de Pepa in Lima, 307 meters in length. Later, in the year 2013, a contest was organized called “The biggest Turron.” Various turron specialists/bakers gathered and prepared a turron 200 meters in length in order to commemorate this magnificent tradition. </p>
                    <p>Even now, exists a commercial candy brand called “Dona Pepa” whose products are sold in every Peruvian shop, and that makes references to the turrones of Dona Pepa for the sprinkles used to decorate their products. </p>
                    <p>Because of this, the Turron de Dona Pepa is no longer sold exclusively in October and can be found all year long in a variety of shops and supermarkets all throughout the country. </p>
                    <br>
                    <h5>But, how is the turron de Dona pepa part of our culture? </h5>
                    <br>
                    <p>Not only is the turron special for religious reasons, it also is part of Peru’s gastronomical culture. </p>
                    <br>
                    <p>Just like a typical Peruvian breakfast consists of bread, coffee and a tamale, at around 5 or 6pm, Peruvians will gather to have a light dinner of bread, tamale, or leftovers from lunch. However, during the first 15 days of October, turron would normally be found on their table instead. This light evening dinner is called a “lonche.” Consumption of turron increases considerably during this religious celebration! </p>
                    <br>
                    <p>Bakeries have a long-standing tradition of preparing these delicious sweets dating back 90 years. San Martín Bakery is the oldest in existence and still use methods from long ago but there are more recent bakeries, like Fausta Pastry shop, located in Lima, that have risen in popularity. </p>
                    <br>
                    <p>Over the years, people have experimented preparing this traditional dessert in different and unique ways. New recipes are being invented all the time! You will likely be able to find a version to satisfy your unique taste buds. Are you looking for a healthier version of this recipe? Try dining out at the restaurant called, El Arandano (blueberry) when in Lima, Peru. On their menu you can find, “The Healthy Turron.” This popular version was introduced in 2020, consisting of almonds, whole wheat flour, honey made from fruits and a replacement of food additives with natural ingredients for a more nutritious option. </p>
                    <br>
                    <p>As time goes by, we can ensure that this unique Peruvian culinary tradition of eating turron will not be lost. Just as society evolves so do traditions. To enjoy this dessert in the traditional way or some other version will make it more accessible to everyone.</p>
                    <p>The turron also caught the attention of a French baker. Madame Kintu, a dessert shop in Lima, introduced the “Kinturron.” This dessert is made with three layers. The base is a sponge cake otherwise known as Genova Mazapan bread. The second layer is creamy filling flavored with honey. The final layer is the classic nougat cookie with sprinkles on top. The photos on their Facebook page of this dessert will definitely make your tummy rumble. </p>
                    <br>
                    <p>Thanks to the ingenuity of Peruvians you can find other variations of this dessert, such as Turron Cheesecake, Turron Peruvian Truffles, and Turron waffles.</p>
                    <br>
                    <p>In this way we see that Doña Pepa's Turrón is not only a tradition in itself, but also, over time, it has served as inspiration for the creation of other desserts or sweets, offering more gastronomic opportunities for both Peruvians and foreigners, and at the same time uniting everyone.</p>
