                    <p>The saying goes, the Peruvian dessert, turron can be traced back to one person, <strong>Josefa Marmanillo</strong>, a slave that lived in the XVII century, who lived near Canete Valley and who’s hands were known to possess the art of cooking. </p>
                    <br>
                    <p>One day, Josefa contracted a rare illness that made it impossible for her to stop moving her arms. Immediately, she went to the capital of Peru, Lima to find a cure for this curious ailment and was brought to pay homage to the statue of <strong>Cristo Moreno, widely known as the Lord of Miracles</strong> or to some, Senor de Pachacamilla, a well-known saint in Peru and who Josefa was extremely devoted to, by attending the procession and other events held in his name.</p>
                    <br>
                    <p>According to legend, thanks to her faith and utter devotion, a miracle was performed. Josefa was cured! She was extremely grateful and in the name of her faith, she decided to create the dessert, turron using flour, sesame seeds, toasted anis, chancaca honey (made from unrefined sugar) and sprinkles as her main ingredients.</p>
                    <br>
                    <p>Ever since then, Dona Josefa who thereafter was endearingly referred to as <strong>Dona Pepa</strong>, would find herself making the long journey to Lima every October for the Lord of Miracles holiday celebration in order to sell and give away her now famous turron. Dona Josefa’s turron became known as <strong>“Turron de Dona Pepa”</strong> as we know it today and is now an essential Peruvian dessert and sweet found on the table of every Peruvian home and is the must-have culinary delight during the important holiday celebration in October throughout Latin America.</p>
                    <br>
                    <p>Turron was so significant in it’s time, that during the Republican era, there existed a job that was specifically dedicated to the preparation and selling of turron and they were given the job title of Turroneros” and/or “Turroneras”. </p>
                    <p>But lets go back in time a bit because the sweet known as turron or at least it’s ingredients do not have it’s origin in Peru, rather Peruvian turron is an <strong>adaptation of Alicante turron brought to Peru by the Spaniards.</strong> It’s base is the nearly identical consisting of almonds, sesame seeds or nuts, mixed with sugar and honey </p>
